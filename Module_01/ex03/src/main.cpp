#include "Weapon.hpp"
#include "HumanA.hpp"
#include "HumanB.hpp"

// Pointer Vs Refererence ex.
// Produces attacks with "crude spiked club"
// THEN "some other type of club", in both test cases

int main()
{
    {
        Weapon club = Weapon("crude spiked club");
        
        HumanA bob("Bob", club);
        bob.attack();
        club.setType("some other type of club");
        bob.attack();
    }
    {
        Weapon club = Weapon("crude spiked club");

        HumanB jim("Jim");
        jim.setWeapon(club);
        jim.attack();
        club.setType("some other type of club");
        jim.attack();
    }
}
